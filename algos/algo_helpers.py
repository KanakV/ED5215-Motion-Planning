
# ------------------------------------------------------------
# Manhattan Distance Heuristic
# ------------------------------------------------------------
def manhattan(a, b):
    return abs(a[0] - b[0]) + abs(a[1] - b[1])

# ------------------------------------------------------------
# Fast squared distance
# ------------------------------------------------------------
def sq_dist(a, b):
    return (a[0]-b[0])**2 + (a[1]-b[1])**2
